// Push notifications (optional)
self.addEventListener('push', (event) => {
  const options = {
    body: event.data ? event.data.text() : 'Nova atualização disponível!',
    icon: './icone-180.png',
    badge: './icone-96.png',
    vibrate: [200, 100, 200],
    tag: 'painel-rep-notification',
    requireInteraction: false
  };

  event.waitUntil(
    self.registration.showNotification('Painel REP', options)
  );
});

// Notification click
self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  event.waitUntil(
    clients.openWindow('./')
  );
});